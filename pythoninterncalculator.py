m=int(input("enter a number"))
n=int(input("enter another number"))
op=input("enter your choice of operation")
if op=="addition":
    print("the calculated value is: ",m+n)
elif op=="subtraction":
    print("the calculated value is: ",m-n)
elif op=="multiplication":
    print("the calculated value is: ",m*n)
elif op=="division":
    print("the calculated value is: ",m/n)
elif op=="floor division":
    print("the calculated value is: ",m//n)
elif op=="remainder":
    print("the calculated value is: ",m%n)
elif op=="exponentiation":
    print("the calculated value is: ",m**n)




